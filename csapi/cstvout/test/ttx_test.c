{
	CSTVOUT_HANDLE tvout_handler;
	CSVOUT_TxtPage_t txtpage;
	int tveid;

	if (argc < 2) {
		printf("Usage: ./ttx_test.elf <tveid>\n");
		return -1;
	}

	tveid = atoi(argv[1]);
	tvout_handler = CSTVOUT_Open(tveid);

	memset(&txtpage, 0, sizeof(CSVOUT_TxtPage_t));	
	memcpy(txtpage.Lines, k_send_Lines[1], 16*42);
	txtpage.ValidLines = 16;
	CSTVOUT_TXT_SetInfo(tvout_handler, &txtpage);

	CSTVOUT_TXT_SetFormat(tvout_handler, VBI_TXT_PALB);

	/* TODO SUNHE: Enable ttx */
	CSTVOUT_TXT_Ctrl(tvout_handler, 1);

	/* Enable df interrupt */
	system("echo \"wl 031 06020001\" >> /proc/vid_io");

	CSTVOUT_Close(tvout_handler);

	return 0;
}
